.MODEL SMALL
.STACK 64
.DATA

MESAJ DB 'BILGISAYAR$'
SUT DB 0DH
CHAR DB 0

.CODE
ANA PROC FAR
    
    MOV AX,@DATA
    MOV DS, AX
    CALL EKRSIL
    LEA SI, MESAJ 
    MOV CX,10 
    
TEKRAR: 

    MOV BL,[SI] 
    MOV CHAR, BL 
    CALL KURKON 
    CALL YAZ 
    INC SI 
    INC SUT
    LOOP TEKRAR 
 
    MOV AH,4CH
    INT 21H

ANA ENDP

KURKON PROC
 
    MOV AH,02
    MOV BH,00
    MOV DH,00
    MOV DL,SUT
    INT 10H 
    RET

KURKON ENDP

YAZ PROC
    
    MOV AH,02
    MOV DL, CHAR
    INT 21H
    RET

YAZ ENDP

EKRSIL PROC
 
    MOV AX,0600H
    MOV BH,30H
    MOV CX,0000H
    MOV DX,284FH
    INT 10H
    RET
    
EKRSIL ENDP
 END ANA